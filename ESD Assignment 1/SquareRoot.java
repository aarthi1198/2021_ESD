/* Java program to find the square root of a number */

import java.lang.Math;
class SquareRoot{
    public static void main(String args[])
	{
	   double a = 30;
	   System.out.println("The square root of " + a + " is " + Math.sqrt(a));
	   a = 45;
	   System.out.println("The square root of " + a + " is " + Math.sqrt(a));
	   a = 60;
	   System.out.println("The square root of " + a + " is " + Math.sqrt(a));
	   a = 90;
	   System.out.println("The square root of " + a + " is " + Math.sqrt(a));
	}
}